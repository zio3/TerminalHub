namespace TerminalHub.Mcp;

/// <summary>
/// MCP サーバーが配布する instructions の既定テンプレ。
///
/// 設計方針:
///  - ここには「反論の余地がない事実（ツールの能力・制約）」と、ユーザーが書き換える前提の
///    「おすすめの運用（＝意見）」を載せる。ハードに縛らず、下書きとして提示するだけ。
///  - ユーザーは設定画面（ローカルMCP タブ）で全文を書き換え・リセットできる。壊しても「既定に戻す」で復旧。
///  - 意見（例: 長文はファイル経由）は固定せず、ユーザーの直接指示・独自ルールが優先される旨を明記する。
///
/// 言語は当面日本語で確定（実利用がほぼ日本語のため）。将来ローカライズしたくなったら、
/// この定数を IStringLocalizer のリソースキーへ差し替えれば言語連動に昇格できる。
/// </summary>
public static class McpInstructionDefaults
{
    public const string Template = """
        # TerminalHub ローカルMCP の使い方

        あなたは TerminalHub が管理する複数ターミナルセッションの1つで動いています。
        この MCP サーバーは、それらのセッション同士を疎につなぐための最小限のツールを提供します。

        ## ツール
        - `list_sessions`: 現在のセッション一覧（表示名・種別・フォルダ・状態）を取得する。宛先を探すのに使う。
        - `send_to_session`: 既存のセッションへメッセージを1件投函する。宛先は GUID か表示名で指定する。
          新しいセッションの起動（spawn）はしない。あくまで「今あるセッションに送る」だけ。

        ## 作法（おすすめ・書き換え可）
        - 投げっぱなしでよい。応答を待って集約するような仕組みは持たない。
        - 大きな仕様・設計・長文を渡すときは、本文をそのまま送らず、ファイルに書いて「そのパスだけ」を
          send_to_session で送ると軽くて確実（受け手にファイルを読ませる）。ただしこれは好みの問題なので、
          直接送りたければ直接送ってよい。
        - 送り先は必ず既存セッション。存在しない相手には送れない。

        ## 優先順位
        上記の「作法」は既定のおすすめに過ぎない。ユーザーからのその場の直接指示や、この下に追記された
        運用ルールがあれば、そちらを優先すること。
        """;
}
