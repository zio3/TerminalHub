namespace TerminalHub.Mcp;

/// <summary>
/// MCP サーバーが配布する instructions の既定テンプレ。
///
/// 設計方針:
///  - ここには「反論の余地がない事実（ツールの能力・制約）」と、ユーザーが書き換える前提の
///    「おすすめの運用（＝意見）」を載せる。ハードに縛らず、下書きとして提示するだけ。
///  - ユーザーは設定画面（ローカルMCP タブ）で全文を書き換え・リセットできる。壊しても「既定に戻す」で復旧。
///  - 意見（例: 長文はファイル経由）は固定せず、ユーザーの直接指示・独自ルールが優先される旨を明記する。
///
/// 言語は当面日本語で確定（実利用がほぼ日本語のため）。将来ローカライズしたくなったら、
/// この定数を IStringLocalizer のリソースキーへ差し替えれば言語連動に昇格できる。
/// </summary>
public static class McpInstructionDefaults
{
    public const string Template = """
        # TerminalHub ローカルMCP の使い方

        あなたは TerminalHub が管理する複数ターミナルセッションの1つで動いています。
        この MCP サーバーは、それらのセッション同士を疎につなぐための最小限のツールを提供します。

        ## 自己識別
        あなた自身のセッション GUID は、環境変数 `TERMINALHUB_SESSION_ID` に入っている。
        自分自身を対象にしたいとき（例: 自分のメモ更新）は、この値を target に渡す。
        ただし CLI によっては tool 実行シェルへ独自の環境変数を渡さない設定があり（例: Codex の
        shell_environment_policy）、その場合 `TERMINALHUB_SESSION_ID` は空に見える。空だったときは、
        `list_sessions` を自分の作業フォルダ・種別で絞り込み、一致する1件のセッション GUID を自分として使えばよい。

        ## ツール
        - `list_sessions`: 現在のセッション一覧（表示名・種別・フォルダ・状態）を取得する。宛先を探すのに使う。
        - `send_to_session`: 既存のセッションへメッセージを1件投函する。宛先は GUID か表示名で指定する。
          新しいセッションの起動（spawn）はしない。あくまで「今あるセッションに送る」だけ。
        - `set_memo`: セッションのメモ（一覧に表示される短い注釈）を設定する。`TERMINALHUB_SESSION_ID` を
          target に渡せば自分のメモを更新できる。「今なにをしているか」を書いておくと一覧から進捗が一目で分かる。

        ## 作法（おすすめ・書き換え可）
        - 投げっぱなしでよい。応答を待って集約するような仕組みは持たない。
        - 大きな仕様・設計・長文を渡すときは、本文をそのまま送らず、ファイルに書いて「そのパスだけ」を
          send_to_session で送ると軽くて確実（受け手にファイルを読ませる）。ただしこれは好みの問題なので、
          直接送りたければ直接送ってよい。
        - 返信がほしいときは、本文に自分のセッションID（`TERMINALHUB_SESSION_ID`、取れなければ list_sessions で
          特定した自分の GUID）を書いておくこと。サーバーは送信元を相手に伝えない（エンベロープなし）ので、
          相手はそれを見て send_to_session で返信できる。「終わったらこの ID に一言返して」等と添えるとよい。
        - 送り先は必ず既存セッション。存在しない相手には送れない。

        ## 優先順位
        上記の「作法」は既定のおすすめに過ぎない。ユーザーからのその場の直接指示や、この下に追記された
        運用ルールがあれば、そちらを優先すること。

        ## 適用範囲・使い分け
        ここに書くのが適切なのは「TerminalHub の MCP ツールをどう使うか」「セッション間でどう連携するか」
        といった、全セッション共通の土台。こういう内容はむしろここに書くとよい。
        一方、これらの instructions が実際に効くのは各セッションが MCP に接続（/ 再接続）したタイミングで、
        いつ反映されるかが少し分かりにくい。なので、プロジェクト固有で確実に効かせたい細かい運用ルールは、
        各 CLI 自身の instructions（例: Claude Code の CLAUDE.md、Codex の AGENTS.md / 設定）に書く方が確実。
        TerminalHub 側で全部を抱え込まず、CLI ネイティブの仕組みと使い分け・併用すること。
        """;
}
